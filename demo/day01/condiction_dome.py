# 最终比较结果用 ' 布尔值 ' 表示
# 大于 >
print(1 > 0)

# 等于 ==
print(1 == 1)

# 小于 <
print(2 < 1)

# 大于等于 >=
print(1 >= 0)

# 小于等于 <=
print(0 <= 1)

# 不等于 !=
print(1 != 0)

# 字符串 元组 列表  只能做 ' == '  和  ' != '  做判断
a = 'aaaa'
b = 'aaaa'
print(a == b)
print(a != b)

# 成员判断 列表 元组 字典 字符串   (判断值是否在这个字段里面)
# 字符串
c = 'sfsafg'
print('sf' in c)

# 列表
q = [1,2,3,4,5,6]
print(1 in q)








